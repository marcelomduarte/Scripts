<#
CMD:
Exibe informações sobre as configurações das interfaces de rede sem fio (Wi-Fi) 
netsh wlan show interfaces

Exibe informações sobre os drivers da interface Wi-Fi
netsh wlan show drivers

Exibe informações sobre os recursos suportados pela interface sem fio
netsh wlan show wirelesscapabilities

Gera um relatório sobre a conectividade sem fio (Wi-Fi)
netsh wlan show wlanreport

Exibe informações detalhadas sobre as configurações, status e perfis de rede sem fio
netsh wlan show all
